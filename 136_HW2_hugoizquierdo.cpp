#include <iostream>

using namespace std;

int main()
{

    string dayName[7] = { "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"};

    for (int count = 0; count < 7; count++)
    {

        cout << dayName[count];
    }
    

    return 0;

}